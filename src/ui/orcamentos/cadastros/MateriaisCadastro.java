    private void editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarActionPerformed
        try {
            MateriaisEditar materiaisEditar = new MateriaisEditar();
            materiaisEditar.setLocationRelativeTo(null);
            materiaisEditar.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            int cod = (int) tabela_consulta.getValueAt(tabela_consulta.getSelectedRow(), 0);
            for (MateriaisCadastroBEAN mcBEAN : MateriaisCadastroDAO.carregaEdicao(cod)) {
                MateriaisEditar.codigo_editar.setValue(mcBEAN.getCod());
                MateriaisEditar.descricao_editar.setText(mcBEAN.getDescricao());
                MateriaisEditar.estoque_seg_editar.setValue(mcBEAN.getEstoque_seg());
                MateriaisEditar.preco_custo_editar.setValue(mcBEAN.getPreco_custo());
                MateriaisEditar.preco_venda_editar.setValue(mcBEAN.getPreco_venda());
            }
            materiaisEditar.setVisible(true);
        } catch (SQLException ex) {
            EnvioExcecao envioExcecao = new EnvioExcecao(Controle.getDefaultGj(), ex);
            EnvioExcecao.envio();
        }
    }//GEN-LAST:event_editarActionPerformed

    private void excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirActionPerformed
        MateriaisCadastroDAO mcDAO = new MateriaisCadastroDAO();
        int cod = (int) tabela_consulta.getValueAt(tabela_consulta.getSelectedRow(), 0);
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(this, "Excluir registro nº " + cod + "?", "Confirmação de exclusão", dialogButton);
        if (dialogResult == 0) {
            try {
                mcDAO.excluir_registro(cod);
                DefaultTableModel modelo_consulta = (DefaultTableModel) tabela_consulta.getModel();
                modelo_consulta.setNumRows(0);
                for (MateriaisCadastroBEAN mcBEAN : MateriaisCadastroDAO.mostraTodos()) {
                    modelo_consulta.addRow(new Object[]{
                        mcBEAN.getCod(),
                        mcBEAN.getDescricao(),
                        mcBEAN.getEstoque_seg(),
                        mcBEAN.getPreco_custo(),
                        mcBEAN.getPreco_venda(),
                        mcBEAN.getData_alteracao()});
                }
            } catch (SQLException ex) {
                EnvioExcecao envioExcecao = new EnvioExcecao(Controle.getDefaultGj(), ex);
            EnvioExcecao.envio();
            }
        }
    }//GEN-LAST:event_excluirActionPerformed

