    private void cadastrar_materialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrar_materialActionPerformed
        try {
            MateriaisCadastroBEAN mcBEAN = new MateriaisCadastroBEAN();
            
            if (descricao_materiais.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Insira um valor no campo Descrição.");
                return;
            } else {
                mcBEAN.setDescricao(descricao_materiais.getText().toUpperCase());
            }
            
            if (estoque_seg_materiais.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Insira um valor no campo Estoque de Segurança.");
                return;
            } else {
                String n2 = String.valueOf(estoque_seg_materiais.getValue().toString());
                mcBEAN.setEstoque_seg(Integer.valueOf(n2));
            }
            
            String n2 = String.valueOf(preco_custo_materiais.getValue().toString());
            n2 = n2.replace(",", ".");
            mcBEAN.setPreco_custo(Float.valueOf(n2));
            
            n2 = String.valueOf(preco_venda_materiais.getValue().toString());
            n2 = n2.replace(",", ".");
            mcBEAN.setPreco_venda(Float.valueOf(n2));
            
            mcBEAN.setData_alteracao(Controle.dataPadrao.format(new Date()));
            
            MateriaisCadastroDAO.create(mcBEAN);
            
            limpa();
        } catch (SQLException ex) {
            EnvioExcecao envioExcecao = new EnvioExcecao(Controle.getDefaultGj(), ex);
            EnvioExcecao.envio();
        }
    }//GEN-LAST:event_cadastrar_materialActionPerformed

