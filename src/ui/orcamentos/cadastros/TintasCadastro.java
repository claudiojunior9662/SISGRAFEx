    private void editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarActionPerformed
        try {
            TintasEditar te = new TintasEditar();
            te.setLocationRelativeTo(null);
            te.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            int cod = (int) tabela_consulta.getValueAt(tabela_consulta.getSelectedRow(), 0);
            for (TintasCadastroBEAN tintasCadastroBEAN : TintasCadastroDAO.carregaEdicao(cod)) {
                TintasEditar.codigo_editar.setValue(tintasCadastroBEAN.getCod());
                TintasEditar.descricao_editar.setText(tintasCadastroBEAN.getDescricao());
                TintasEditar.fator_absorcao_editar.setValue(tintasCadastroBEAN.getFator_absorcao());
                TintasEditar.valor_kg_editar.setValue(tintasCadastroBEAN.getValor_kg());
            }
            te.setVisible(true);
        } catch (SQLException ex) {
            EnvioExcecao envioExcecao = new EnvioExcecao(Controle.getDefaultGj(), ex);
            EnvioExcecao.envio(null);
        }
    }//GEN-LAST:event_editarActionPerformed

    private void excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirActionPerformed
        TintasCadastroDAO tcDAO = new TintasCadastroDAO();
        int cod = (int) tabela_consulta.getValueAt(tabela_consulta.getSelectedRow(), 0);
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(this, "Excluir registro nº " + cod + "?", "Confirmação de exclusão", dialogButton);
        if (dialogResult == 0) {
            try {
                tcDAO.exclui_registro(cod);
                DefaultTableModel modelo_consulta = (DefaultTableModel) tabela_consulta.getModel();
                modelo_consulta.setNumRows(0);
                for (TintasCadastroBEAN tcBEAN : TintasCadastroDAO.mostraTodos()) {
                    modelo_consulta.addRow(new Object[]{
                        tcBEAN.getCod(),
                        tcBEAN.getDescricao(),
                        tcBEAN.getFator_absorcao(),
                        tcBEAN.getValor_kg()});
                }
            } catch (SQLException ex) {
                EnvioExcecao envioExcecao = new EnvioExcecao(Controle.getDefaultGj(), ex);
                EnvioExcecao.envio(null);
            }
        }
    }//GEN-LAST:event_excluirActionPerformed

