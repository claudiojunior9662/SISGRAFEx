    private void cadastrar_tintasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrar_tintasActionPerformed
        try {
            TintasCadastroBEAN tintasCadastroBEAN = new TintasCadastroBEAN();

            if (descricao_tintas.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Insira um valor no campo Descrição.");
                return;
            } else {
                tintasCadastroBEAN.setDescricao(descricao_tintas.getText().toUpperCase());
            }

            String n2 = String.valueOf(fator_absorcao_tintas.getValue().toString());
            n2 = n2.replace(",", ".");
            tintasCadastroBEAN.setFator_absorcao(Float.valueOf(n2));

            n2 = String.valueOf(valor_kg_tintas.getValue().toString());
            n2 = n2.replace(",", ".");
            tintasCadastroBEAN.setValor_kg(Float.valueOf(n2));

            TintasCadastroDAO.create(tintasCadastroBEAN);
        } catch (SQLException ex) {
            EnvioExcecao envioExcecao = new EnvioExcecao(Controle.getDefaultGj(), ex);
            EnvioExcecao.envio(null);
        }
    }//GEN-LAST:event_cadastrar_tintasActionPerformed

