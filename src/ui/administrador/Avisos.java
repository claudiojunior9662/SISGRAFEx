    public Avisos(JLabel loading) {
        initComponents();
        this.loading = loading;

        new Thread("Carrega avisos") {
            @Override
            public void run() {
                try {
                    List<String> avisos = Controle.retornaAvisos();
                    for (int i = 0; i < avisos.size(); i++) {
                        switch (i) {
                            case 0:
                                taAvOrc.setText(avisos.get(i));
                                break;
                            case 1:
                                taAvProd.setText(avisos.get(i));
                                break;
                            case 2:
                                taAvExp.setText(avisos.get(i));
                                break;
                            case 3:
                                taAvFin.setText(avisos.get(i));
                                break;
                            case 4:
                                taAvOrd.setText(avisos.get(i));
                                break;
                        }
                    }
                } catch (SQLException ex) {
                    EnvioExcecao envioExcecao = new EnvioExcecao(Controle.getDefaultGj(), ex);
                    EnvioExcecao.envio();
                }
            }
        }.start();
    }

