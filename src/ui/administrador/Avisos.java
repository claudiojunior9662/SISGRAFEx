    private void btnAtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtActionPerformed
        new Thread("Atualiza avisos") {
            @Override
            public void run() {
                try {
                    loading.setVisible(true);
                    loading.setText("ATUALIZANDO...");
                    List<String> avisos = new ArrayList();
                    avisos.add(taAvOrc.getText());
                    avisos.add(taAvProd.getText());
                    avisos.add(taAvExp.getText());
                    avisos.add(taAvFin.getText());
                    avisos.add(taAvOrd.getText());
                    Controle.atualizaAvisos(avisos);
                    JOptionPane.showMessageDialog(null,
                            "AVISOS ATUALIZADOS COM SUCESSO",
                            "CONFIRMAÇÃO",
                            JOptionPane.INFORMATION_MESSAGE);
                } catch (SQLException ex) {
                    EnvioExcecao envioExcecao = new EnvioExcecao(Controle.getDefaultGj(), ex);
                    EnvioExcecao.envio();
                }
                loading.setVisible(false);
            }
        }.start();
    }//GEN-LAST:event_btnAtActionPerformed


