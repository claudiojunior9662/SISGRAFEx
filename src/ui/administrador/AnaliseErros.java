    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        try {
            ErroDAO.excluiErr(erro.getId());
            JOptionPane.showMessageDialog(null, "EXCEÇÃO EXCLUÍDA COM SUCESSO!",
                    "ATENÇÃO",
                    JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "A OPERAÇÃO NÃO PODE SER REALIZADA NO MOMENTO."
                    + "\nTENTE NOVAMENTE MAIS TARDE. " + ex,
                    "ERRO",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

