    private void atualizaTabela() {
        new Thread("Carrega erros") {
            @Override
            public void run() {
                try {
                    modelo.setNumRows(0);
                    for (ErroBEAN erro : ErroDAO.retornaErros()) {
                        modelo.addRow(erro);
                    }
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, "A OPERAÇÃO NÃO PODE SER REALIZADA NO MOMENTO."
                            + "\nTENTE NOVAMENTE MAIS TARDE. " + ex,
                            "ERRO",
                            JOptionPane.ERROR_MESSAGE);
                }
            }
        }.start();
    }
