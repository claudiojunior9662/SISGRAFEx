    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        byte anterior = Controle.getTipoVersao();
        if (rdProducao.isSelected()) {
            Controle.setTipoVersao((byte) 1);
            if (ConnectionFactory.getConnection() == null) {
                JOptionPane.showMessageDialog(null,
                        "NÃO FOI POSSÍVEL OBTER UMA CONEXÃO COM A EB NET.\n"
                        + "VERIFIQUE A SUA CONEXÃO E TENTE NOVAMENTE.",
                        "ATENÇÃO!",
                        JOptionPane.ERROR_MESSAGE);
                Controle.setTipoVersao(anterior);
                return;
            }
            JOptionPane.showMessageDialog(null,
                    "BASE DE DADOS ALTERADA PARA PRODUÇÃO.\n"
                    + "POR FAVOR, FAÇA LOGIN EM PRODUÇÃO.",
                    "ATENÇÃO!",
                    JOptionPane.INFORMATION_MESSAGE);
            Controle.setTipoLogin((byte) 2);
            TelaAutenticacao ta = new TelaAutenticacao();
            ta.setDefaultCloseOperation(EXIT_ON_CLOSE);
            ta.setLocationRelativeTo(null);
            ta.setVisible(true);
            this.dispose();
        } else if (rdDevRede.isSelected()) {
            Controle.setTipoVersao((byte) 2);
            if (ConnectionFactory.getConnection() == null) {
                JOptionPane.showMessageDialog(null,
                        "NÃO FOI POSSÍVEL OBTER UMA CONEXÃO COM A EB NET.\n"
                        + "VERIFIQUE A SUA CONEXÃO E TENTE NOVAMENTE.",
                        "ATENÇÃO!",
                        JOptionPane.ERROR_MESSAGE);
                Controle.setTipoVersao(anterior);
                return;
            }
            JOptionPane.showMessageDialog(null,
                    "BASE DE DADOS ALTERADA PARA DESENVOLVIMENTO EM REDE.\n"
                    + "POR FAVOR, FAÇA LOGIN EM DESENVOLVIMENTO EM REDE.",
                    "ATENÇÃO!",
                    JOptionPane.INFORMATION_MESSAGE);
            Controle.setTipoLogin((byte) 2);
            TelaAutenticacao ta = new TelaAutenticacao();
            ta.setDefaultCloseOperation(EXIT_ON_CLOSE);
            ta.setLocationRelativeTo(null);
            ta.setVisible(true);
            this.dispose();
        } else if (rdDevLocal.isSelected()) {
            Controle.setTipoVersao((byte) 3);
            if (ConnectionFactory.getConnection() == null) {
                JOptionPane.showMessageDialog(null,
                        "NÃO FOI POSSÍVEL OBTER UMA CONEXÃO COM O SERVIDOR LOCAL.",
                        "ATENÇÃO!",
                        JOptionPane.ERROR_MESSAGE);
                Controle.setTipoVersao(anterior);
                return;
            }
            JOptionPane.showMessageDialog(null,
                    "BASE DE DADOS ALTERADA PARA DESENVOLVIMENTO LOCAL.\n"
                    + "POR FAVOR, FAÇA LOGIN EM DESENVOLVIMENTO LOCAL.",
                    "ATENÇÃO!",
                    JOptionPane.INFORMATION_MESSAGE);
            Controle.setTipoLogin((byte) 2);
            TelaAutenticacao ta = new TelaAutenticacao();
            ta.setDefaultCloseOperation(EXIT_ON_CLOSE);
            ta.setLocationRelativeTo(null);
            ta.setVisible(true);
            this.dispose();
        }
        Controle.defineStatus(statusPane);
    }//GEN-LAST:event_btnConfirmarActionPerformed


