    public void atualizaTabela() {
        try {
            DefaultTableModel modeloErro = (DefaultTableModel) tabelaErros.getModel();
            modeloErro.setNumRows(0);
            for (ErroBEAN bean : ErroDAO.retornaErros()) {
                modeloErro.addRow(new Object[]{
                    bean.getId(),
                    bean.getUsr(),
                    bean.getDescricao(),
                    bean.getSts()
                });
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "A OPERAÇÃO NÃO PODE SER REALIZADA NO MOMENTO."
                    + "\nTENTE NOVAMENTE MAIS TARDE. " + ex,
                    "ERRO",
                    JOptionPane.ERROR_MESSAGE);
        }
    }
