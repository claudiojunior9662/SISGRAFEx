    private void confirmar_edicaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmar_edicaoActionPerformed
        try {
            Chapa cp = new Chapa();
            ChapaDAO cpDAO = new ChapaDAO();
            
            if(descricao_editar.getText().equals(null)){
                JOptionPane.showMessageDialog(null, "Insira um valor no campo Descrição.");
                return;
            }else{
                cp.setDescricao(descricao_editar.getText().toUpperCase());
            }
            
            if(largura_editar.getValue().equals(null)){
                JOptionPane.showMessageDialog(null, "Insira um valor no campo Largura.");
                return;
            }else{
                String n2 = String.valueOf(largura_editar.getValue().toString());
                n2 = n2.replace(",", ".");
                float n3 = Float.valueOf(n2);
                cp.setLargura(n3);
            }
            
            if(altura_editar.getValue().equals(null)){
                JOptionPane.showMessageDialog(null, "Insira um valor no campo Altura.");
                return;
            }else{
                String n2 = String.valueOf(altura_editar.getValue().toString());
                n2 = n2.replace(",", ".");
                float n3 = Float.valueOf(n2);
                cp.setAltura(n3);
            }
            
            if(durabilidade_editar.getValue().equals(null)){
                JOptionPane.showMessageDialog(null, "Insira um valor no campo Durabilidade.");
                return;
            }else{
                String n2 = String.valueOf(durabilidade_editar.getValue().toString());
                cp.setDurabilidade(Integer.valueOf(n2));
            }
            
            if(tipo_editar.getText().equals(null)){
                JOptionPane.showMessageDialog(null, "Insira um valor no campo Tipo.");
                return;
            }else{
                cp.setTipo(tipo_editar.getText().toUpperCase());
            }
            
            if(valor_editar.getValue().equals(null)){
                JOptionPane.showMessageDialog(null, "Insira um valor no campo Valor Unitárío.");
                return;
            }else{
                String n2 = String.valueOf(valor_editar.getValue().toString());
                n2 = n2.replace(",", ".");
                float n3 = Float.valueOf(n2);
                cp.setValor_unitario(n3);
            }
            
            cpDAO.editarRegistro((int) codigo_editar.getValue(), cp);
            
            DefaultTableModel modeloConsulta = (DefaultTableModel) ChapaCadastro.tabela_consulta.getModel();
            modeloConsulta.setNumRows(0);
            
            this.dispose();
        } catch (SQLException ex) {
            EnvioExcecao envioExcecao = new EnvioExcecao(Controle.getDefaultGj(), ex);
            EnvioExcecao.envio();
        }
    }//GEN-LAST:event_confirmar_edicaoActionPerformed

