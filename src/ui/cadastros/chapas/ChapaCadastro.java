    private void cadastrar_chapaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrar_chapaActionPerformed
        ChapaBEAN cc = new ChapaBEAN();
        ChapaDAO ccDAO = new ChapaDAO();

        if (descricao_chapa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Insira um valor no campo Descrição.");
            return;
        } else {
            cc.setDescricao(descricao_chapa.getText().toUpperCase());
        }

        if (largura_chapa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Insira um valor no campo Largura.");
            return;
        } else {
            String n2 = String.valueOf(largura_chapa.getValue().toString());
            n2 = n2.replace(",", ".");
            float n3 = Float.valueOf(n2);
            cc.setLargura(n3);
        }

        if (altura_chapa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Insira um valor no campo Altura.");
            return;
        } else {
            String n2 = String.valueOf(altura_chapa.getValue().toString());
            n2 = n2.replace(",", ".");
            float n3 = Float.valueOf(n2);
            cc.setAltura(n3);
        }

        if (durabilidade_chapa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Insira um valor no campo Durabilidade.");
            return;
        } else {
            String n2 = String.valueOf(durabilidade_chapa.getValue().toString());
            cc.setDurabilidade(Integer.valueOf((n2)));
        }

        if (tipo_chapa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Insira um valor no campo Tipo.");
            return;
        } else {
            cc.setTipo(tipo_chapa.getText().toUpperCase());
        }

        if (valor_chapa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Insira um valor no campo Valor Unitário.");
            return;
        } else {
            String n2 = String.valueOf(valor_chapa.getValue().toString());
            n2 = n2.replace(",", ".");
            float n3 = Float.valueOf(n2);
            cc.setValor_unitario(n3);
        }

        try {
            ccDAO.create(cc);
        } catch (SQLException ex) {
            EnvioExcecao envioExcecao = new EnvioExcecao(Controle.getDefaultGj(), ex);
            EnvioExcecao.envio();
        }

        limpa();
    }//GEN-LAST:event_cadastrar_chapaActionPerformed

    private void botao_pesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_pesquisarActionPerformed
        try {
            DefaultTableModel modelo_consulta = (DefaultTableModel) tabela_consulta.getModel();
            modelo_consulta.setNumRows(0);
            ChapaDAO cpDAO = new ChapaDAO();
            String tp = (String) tipo_pesquisa.getSelectedItem();
            String p = texto_pesquisa.getText();
            for (Chapa cp : cpDAO.consulta(tp, p)) {
                modelo_consulta.addRow(new Object[]{
                    cp.getCod(),
                    cp.getDescricao(),
                    cp.getLargura(),
                    cp.getAltura(),
                    cp.getDurabilidade(),
                    cp.getTipo(),
                    cp.getValor_unitario()});
            }
        } catch (SQLException ex) {
            EnvioExcecao envioExcecao = new EnvioExcecao(Controle.getDefaultGj(), ex);
            EnvioExcecao.envio();
        }
    }//GEN-LAST:event_botao_pesquisarActionPerformed

