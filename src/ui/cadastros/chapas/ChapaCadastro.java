    private void excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirActionPerformed
        ChapaDAO cpDAO = new ChapaDAO();
        int cod = (int) tabela_consulta.getValueAt(tabela_consulta.getSelectedRow(), 0);
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(this, "Excluir registro nº " + cod + "?", "Confirmação de exclusão", dialogButton);
        if (dialogResult == 0) {
            try {
                cpDAO.exclui_registro(cod);
                DefaultTableModel modelo_consulta = (DefaultTableModel) tabela_consulta.getModel();
                modelo_consulta.setNumRows(0);
                for (Chapa cp : cpDAO.mostra_todos()) {
                    modelo_consulta.addRow(new Object[]{
                        cp.getCod(),
                        cp.getDescricao(),
                        cp.getLargura(),
                        cp.getAltura(),
                        cp.getDurabilidade(),
                        cp.getTipo(),
                        cp.getValor_unitario()});
                }
            } catch (SQLException ex) {
                EnvioExcecao envioExcecao = new EnvioExcecao(Controle.getDefaultGj(), ex);
                EnvioExcecao.envio();
            }
        }
    }//GEN-LAST:event_excluirActionPerformed

    private void editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarActionPerformed
        try {
            ChapaDAO cpDAO = new ChapaDAO();
            int cod = (int) tabela_consulta.getValueAt(tabela_consulta.getSelectedRow(), 0);
            ChapasEditar ce = new ChapasEditar();
            ce.setLocationRelativeTo(null);
            ce.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            for (Chapa cp : cpDAO.carregar_edicao(cod)) {
                ChapasEditar.codigo_editar.setValue(cp.getCod());
                ChapasEditar.descricao_editar.setText(cp.getDescricao());
                ChapasEditar.largura_editar.setValue(cp.getLargura());
                ChapasEditar.altura_editar.setValue(cp.getAltura());
                ChapasEditar.durabilidade_editar.setValue(cp.getDurabilidade());
                ChapasEditar.tipo_editar.setText(cp.getTipo());
                ChapasEditar.valor_editar.setValue(cp.getValor_unitario());

            }
            ce.setVisible(true);
        } catch (SQLException ex) {
            EnvioExcecao envioExcecao = new EnvioExcecao(Controle.getDefaultGj(), ex);
            EnvioExcecao.envio();
        }
    }//GEN-LAST:event_editarActionPerformed

