    private void botaoAdicionarTabelaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAdicionarTabelaActionPerformed
        if (selecionarClientes == true) {
            DefaultTableModel modeloContatos = (DefaultTableModel) ClienteCadastro.tabelaEnderecos.getModel();
            modeloContatos.addRow(new Object[]{
                tabelaPesquisa.getValueAt(tabelaPesquisa.getSelectedRow(), 0),
                tabelaPesquisa.getValueAt(tabelaPesquisa.getSelectedRow(), 2),
                tabelaPesquisa.getValueAt(tabelaPesquisa.getSelectedRow(), 1),
                tabelaPesquisa.getValueAt(tabelaPesquisa.getSelectedRow(), 3),
                tabelaPesquisa.getValueAt(tabelaPesquisa.getSelectedRow(), 4),
                tabelaPesquisa.getValueAt(tabelaPesquisa.getSelectedRow(), 5),
                tabelaPesquisa.getValueAt(tabelaPesquisa.getSelectedRow(), 6),
                tabelaPesquisa.getValueAt(tabelaPesquisa.getSelectedRow(), 7),});
            ClienteCadastro.tabelaEnderecos.setEnabled(true);
            this.dispose();
        }
        if (selecionarTransportadoras == true) {
            DefaultTableModel modeloContatos = (DefaultTableModel) CadastroTransportadoras.tabelaEnderecos.getModel();
            modeloContatos.addRow(new Object[]{
                tabelaPesquisa.getValueAt(tabelaPesquisa.getSelectedRow(), 0),
                tabelaPesquisa.getValueAt(tabelaPesquisa.getSelectedRow(), 2),
                tabelaPesquisa.getValueAt(tabelaPesquisa.getSelectedRow(), 1),
                tabelaPesquisa.getValueAt(tabelaPesquisa.getSelectedRow(), 3),
                tabelaPesquisa.getValueAt(tabelaPesquisa.getSelectedRow(), 4),
                tabelaPesquisa.getValueAt(tabelaPesquisa.getSelectedRow(), 5),
                tabelaPesquisa.getValueAt(tabelaPesquisa.getSelectedRow(), 6),
                tabelaPesquisa.getValueAt(tabelaPesquisa.getSelectedRow(), 7),});
            CadastroTransportadoras.tabelaEnderecos.setEnabled(true);
            this.dispose();
        }

    }//GEN-LAST:event_botaoAdicionarTabelaActionPerformed

    private void p1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_p1ItemStateChanged
//        TIPO ENDEREÇO (RESIDENCIAL)
//        TIPO ENDEREÇO (COMERCIAL)
        if (p1.getSelectedItem().toString().equals("TIPO ENDEREÇO (RESIDENCIAL)")) {
            p2.setEnabled(false);
        } else if (p1.getSelectedItem().toString().equals("TIPO ENDEREÇO (COMERCIAL)")) {
            p2.setEnabled(false);
        } else {
            p2.setEnabled(true);
        }
    }//GEN-LAST:event_p1ItemStateChanged

    private void botaoDesativarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoDesativarActionPerformed
        CODIGO_ENDERECO = Integer.valueOf(tabelaPesquisa.getValueAt(tabelaPesquisa.getSelectedRow(), 0).toString());

        try {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(this, "ESTA AÇÃO PODERÁ AFETAR OUTROS CLIENTES RELACIONADOS A ESTE ENDEREÇO.\nDESEJA CONTINUAR?", "ATENÇÃO!", dialogButton);
            if (dialogResult == 0) {
                ClienteDAO.desativaAtivaEnderecos(0,
                        CODIGO_ENDERECO,
                        (byte) 0,
                        (byte) 1,
                        false);
            }
            botaoDesativar.setEnabled(false);

            DefaultTableModel modeloPesquisa = (DefaultTableModel) tabelaPesquisa.getModel();
            modeloPesquisa.setNumRows(0);
            for (EnderecoBEAN auxBEAN : ClienteDAO.retornaPesquisaEnderecos("", "", true)) {
                modeloPesquisa.addRow(new Object[]{
                    auxBEAN.getCodigo(),
                    auxBEAN.getCep(),
                    auxBEAN.getTipoEndereco(),
                    auxBEAN.getLogadouro(),
                    auxBEAN.getCidade(),
                    auxBEAN.getBairro(),
                    auxBEAN.getUf(),
                    auxBEAN.getComplemento()
                });
            }
        } catch (SQLException ex) {
            EnvioExcecao envioExcecao = new EnvioExcecao(Controle.getDefaultGj(), ex);
            EnvioExcecao.envio();
        }
    }//GEN-LAST:event_botaoDesativarActionPerformed

