package ui.cadastros.notas;

/**
 *
 * @author claudio
 */
public class VolumeBEAN {
    private int cod;
    private int codTransporte;
    private int numeroVolume;
    private float alturaVolume;
    private float larguraVolume;
    private float pesoVolume;

    public int getCod() {
        return cod;
    }

    public void setCod(int cod) {
        this.cod = cod;
    }

    public int getCodTransporte() {
        return codTransporte;
    }

    public void setCodTransporte(int codTransporte) {
        this.codTransporte = codTransporte;
    }

    public int getNumeroVolume() {
        return numeroVolume;
    }

    public void setNumeroVolume(int numeroVolume) {
        this.numeroVolume = numeroVolume;
    }

    public float getAlturaVolume() {
        return alturaVolume;
    }

    public void setAlturaVolume(float alturaVolume) {
        this.alturaVolume = alturaVolume;
    }

    public float getLarguraVolume() {
        return larguraVolume;
    }

    public void setLarguraVolume(float larguraVolume) {
        this.larguraVolume = larguraVolume;
    }

    public float getPesoVolume() {
        return pesoVolume;
    }

    public void setPesoVolume(float pesoVolume) {
        this.pesoVolume = pesoVolume;
    }
    
    
}
