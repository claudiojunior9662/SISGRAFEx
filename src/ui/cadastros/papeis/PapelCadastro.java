    private void editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarActionPerformed
        PapelDAO pcDAO = new PapelDAO();
        int cod = (int) tabelaConsulta.getValueAt(tabelaConsulta.getSelectedRow(), 0);
        PapelEditar pe = new PapelEditar();
        pe.setLocationRelativeTo(null);
        pe.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
        pe.setTitle("EDIÇÃO DE PAPÉIS");
        for (Papel pcBEAN : pcDAO.carregar_edicao(cod)) {
            float largura = 0;
            float altura = 0;
            String medida;
            int index_x = 0;
            if (pcBEAN.getMedida().contains("X")) {
                index_x = pcBEAN.getMedida().indexOf("X");
            } else if (pcBEAN.getMedida().contains("x")) {
                index_x = pcBEAN.getMedida().indexOf("x");
            }
            largura = Float.valueOf(pcBEAN.getMedida().substring(0, index_x - 1));
            altura = Float.valueOf(pcBEAN.getMedida().substring(index_x + 2));
            PapelEditar.codigoEditar.setValue(pcBEAN.getCod());
            PapelEditar.descricaoEditar.setText(pcBEAN.getDescricao());
            PapelEditar.larguraEditar.setValue(largura);
            PapelEditar.alturaEditar.setValue(altura);
            PapelEditar.gramaturaEditar.setValue(pcBEAN.getGramatura());
            PapelEditar.formato.setText(pcBEAN.getFormato());
            if (pcBEAN.getUma_face() == 1) {
                umaFaceCheck.setSelected(true);
            } else {
                umaFaceCheck.setSelected(false);
            }
            PapelEditar.vlrUnit.setValue(pcBEAN.getUnitario());
        }
        pe.setVisible(true);
    }//GEN-LAST:event_editarActionPerformed

    private void excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirActionPerformed
        PapelDAO pcDAO = new PapelDAO();
        int cod = (int) tabelaConsulta.getValueAt(tabelaConsulta.getSelectedRow(), 0);
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(this, "EXCLUIR REGISTRO Nº " + cod + " ?", "CONFIRMAÇÃO DE EXCLUSÃO", dialogButton);
        if (dialogResult == 0) {
            pcDAO.excluir_registro(cod);
            DefaultTableModel modelo_consulta = (DefaultTableModel) tabelaConsulta.getModel();
            modelo_consulta.setNumRows(0);
            for (Papel pcBEAN : pcDAO.mostraTodos()) {
                modelo_consulta.addRow(new Object[]{
                    pcBEAN.getCod(),
                    pcBEAN.getDescricao(),
                    pcBEAN.getMedida(),
                    pcBEAN.getGramatura(),
                    pcBEAN.getFormato(),
                    pcBEAN.getUma_face(),
                    pcBEAN.getUnitario()});
            }
        }
    }//GEN-LAST:event_excluirActionPerformed

