    private void cadastrar_papelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrar_papelActionPerformed
        try {
            Papel papel = new Papel();

            String medida;
            int largura;
            int altura;
            int uma_face;
            float valor_unitario;

            if (descricaoPapel.getText().equals(null)) {
                JOptionPane.showMessageDialog(null, "Insira um valor no campo Descrição.");
                return;
            } else {
                papel.setDescricao(descricaoPapel.getText().toUpperCase());
            }

            if (larguraPapel.getValue().equals(null)) {
                JOptionPane.showMessageDialog(null, "Insira um valor no campo Largura.");
                return;
            } else {
                String n2 = String.valueOf(larguraPapel.getValue().toString());
                largura = Integer.valueOf(n2);
            }

            if (alturaPapel.getValue().equals(null)) {
                JOptionPane.showMessageDialog(null, "Insira um valor no campo Altura.");
                return;
            } else {
                String n2 = String.valueOf(alturaPapel.getValue().toString());
                altura = Integer.valueOf(n2);
            }

            medida = largura + " X " + altura;
            System.out.println(medida);
            papel.setMedida(medida);

            if (gramaturaPapel.getValue().equals(null)) {
                JOptionPane.showMessageDialog(null, "Insira um valor no campo Gramatura.");
                return;
            } else {
                String n2 = String.valueOf(gramaturaPapel.getValue().toString());
                papel.setGramatura(Integer.valueOf(n2));
            }

            if (formatoPapel.getText().equals(null)) {
                JOptionPane.showMessageDialog(null, "Insira um valor no campo Formato.");
                return;
            } else {
                papel.setFormato(formatoPapel.getText().toUpperCase());
            }

            if (umaFaceCheck.isSelected()) {
                uma_face = 1;
            } else {
                uma_face = 0;
            }

            if (valorPapel.getValue().equals(null)) {
                JOptionPane.showMessageDialog(null, "Insira um valor no campo Valor Unitário.");
                return;
            } else {
                String n2 = String.valueOf(valorPapel.getValue().toString());
                n2 = n2.replace(",", ".");
                double n3 = Double.valueOf(n2);
                papel.setUnitario((float) n3);
            }

            PapelDAO.create(papel);

            limpa();
        } catch (SQLException ex) {
            EnvioExcecao envioExcecao = new EnvioExcecao(Controle.getDefaultGj(), ex);
            EnvioExcecao.envio(null);
        }
    }//GEN-LAST:event_cadastrar_papelActionPerformed

