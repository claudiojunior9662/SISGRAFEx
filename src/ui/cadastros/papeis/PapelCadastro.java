    private void botao_pesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_pesquisarActionPerformed
        try {
            DefaultTableModel modelo_consulta = (DefaultTableModel) tabelaConsulta.getModel();
            modelo_consulta.setNumRows(0);
            String tipo = (String) tipo_pesquisa.getSelectedItem();
            String texto = textoPesquisa.getText();

            for (Papel papel : PapelDAO.pesquisaRegistro(tipo, texto)) {
                String i;
                if (papel.getUma_face() == 1) {
                    i = "SIM";
                } else {
                    i = "NÃO";
                }
                modelo_consulta.addRow(new Object[]{
                    papel.getCod(),
                    papel.getDescricao(),
                    papel.getMedida(),
                    papel.getGramatura(),
                    papel.getFormato(),
                    i,
                    papel.getUnitario()});
            }
        } catch (SQLException ex) {
            EnvioExcecao envioExcecao = new EnvioExcecao(Controle.getDefaultGj(), ex);
            EnvioExcecao.envio(null);
        }
    }//GEN-LAST:event_botao_pesquisarActionPerformed

