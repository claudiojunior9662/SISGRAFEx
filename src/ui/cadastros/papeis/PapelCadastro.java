    private void selecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selecionarActionPerformed
        PapelDAO pcDAO = new PapelDAO();
        if (listaPrecoPapeis) {
            for (Papel pcBEAN : pcDAO.carregaOrcamento((int) tabelaConsulta.getValueAt(tabelaConsulta.getSelectedRow(), 0))) {
                ProdutoFrame.codigoPapel.setValue(pcBEAN.getCod());
                ProdutoFrame.descricaoPapel = pcBEAN.getDescricao();
                ProdutoFrame.tipoPapel.setEnabled(true);
                ProdutoFrame.coresFrente.setEnabled(true);
                ProdutoFrame.coresVerso.setEnabled(true);
                ProdutoFrame.concluirPapel.setEnabled(true);
                if (ProdutoFrame.jcbTipoProduto.toString().equals("LIVRO") && ProdutoFrame.inserir == true) {
                    ProdutoFrame.orelha.setEnabled(true);
                }
            }
        }
        listaPrecoPapeisEditar = false;
        listaPrecoPapeis = false;

        this.dispose();
    }//GEN-LAST:event_selecionarActionPerformed

