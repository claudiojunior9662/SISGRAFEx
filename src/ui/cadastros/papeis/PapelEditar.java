    private void confirmar_alteracoesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmar_alteracoesActionPerformed
        Papel pcBEAN = new Papel();
        PapelDAO pcDAO = new PapelDAO();
        String medida;
        int uma_face;

        if (descricaoEditar.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Insira um valor no campo Descricao.");
            return;
        } else {
            pcBEAN.setDescricao(descricaoEditar.getText());
        }

        medida = larguraEditar.getText() + " X " + alturaEditar.getText();
        pcBEAN.setMedida(medida);

        if (gramaturaEditar.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Insira um valor no campo Gramatura.");
            return;
        } else {
            String n2 = String.valueOf(gramaturaEditar.getValue().toString());
            pcBEAN.setGramatura(Integer.valueOf(n2));
        }

        if (formato.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Insira um valor no campo Formato.");
            return;
        } else {
            pcBEAN.setFormato(formato.getText());
        }

        if (umaFace.isSelected()) {
            uma_face = 1;
        } else {
            uma_face = 0;
        }

        pcBEAN.setUma_face(uma_face);

        if (vlrUnit.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Insira um valor no campo Valor Unitário.");
            return;
        } else {
            String n2 = String.valueOf(vlrUnit.getValue().toString());
            n2 = n2.replace(",", ".");
            double n3 = Double.valueOf(n2);
            pcBEAN.setUnitario((float) n3);

        }

        pcDAO.altera_registro((int) codigoEditar.getValue(), pcBEAN);

        DefaultTableModel modelo_consulta = (DefaultTableModel) PapelCadastro.tabelaConsulta.getModel();
        modelo_consulta.setNumRows(0);

        this.dispose();
    }//GEN-LAST:event_confirmar_alteracoesActionPerformed

